#include "LightSource.h"

////////////////////////////////////////////////////////////////////////////////
// Class Constructor Functions
LightSource::LightSource()
{

}
////////////////////////////////////////////////////////////////////////////////
// Get Member Functions
PointLight LightSource::getPointLight()
{
  return pointLight;
}
////////////////////////////////////////////////////////////////////////////////
// Set Member Functions
void LightSource::setPointLight(PointLight newPointLight)
{
  pointLight = newPointLight;
}
////////////////////////////////////////////////////////////////////////////////
void LightSource::applyViewMatrix(Matrix4 viewMatrix)
{
  if (pointLight.getState() == true)
  {
    pointLight.setPosition(viewMatrix * pointLight.getPosition());
  }
}
